namespace Discord.Enums
{
    /// <summary>
    /// Guild default message notification level
    /// </summary>
    public enum NotificationLevel : int
    {
        /// <summary>
        /// All messages cause notifications
        /// </summary>
        ALL_MESSAGES = 0,
        /// <summary>
        /// Only mentions cause notifications
        /// </summary>
        ONLY_MENTIONS = 1
    }
}
